CubeMap::CubeMap(const std::string& dir)
{
	//assumes that images are named, front, back, right...
	
	//TODO setup cube vao
	glGenBuffers(1, &VBO);
	glGenVertexArrays(1, &VAO);
	glBindVertexArray(VAO);
	glBindBuffer(GL_ARRAY_BUFFER, VBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), skyboxVertices, GL_STATIC_DRAW);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
	glEnableVertexAttribArray(0);


	std::cout << "Loading cubemap from " << dir << "\n";
	std::vector<std::string> faces =
	{
		dir + "right.jpg",
		dir + "left.jpg",
		dir + "top.jpg",
		dir + "bottom.jpg",
		dir + "front.jpg",
		dir + "back.jpg"
	};
	stbi_set_flip_vertically_on_load(false);
	glGenTextures(1, &textureID);
	glBindTexture(GL_TEXTURE_CUBE_MAP, textureID);

	for (unsigned int i = 0; i < faces.size(); i++) {
		unsigned char* data = stbi_load(faces[i].c_str(), &width, &height, &nrChannels, 0);
		if (data) {
			glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_RGB, width, height, 0, GL_RGB, GL_UNSIGNED_BYTE, data);
			stbi_image_free(data);
		}
		else {
			std::cout << "Cubemap texture failed to load from path: " << faces[i] << "\n";
		}
	}
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);
	
}

void CubeMap::bind()
{
	glBindTexture(GL_TEXTURE_CUBE_MAP, textureID);

}

void CubeMap::draw(Shader shader, glm::mat4 projection, glm::mat4 view)
{
	glDepthMask(GL_FALSE);
	shader.UseProgram();
	//setup uniforms
	shader.setUniformMat4f("projection", projection);
	shader.setUniformMat4f("view", glm::mat4(glm::mat3(view)));
	glBindVertexArray(VAO);
	CubeMap::bind();
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glDepthMask(GL_TRUE);

}
